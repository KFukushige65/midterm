function plus(m, n){
    let result;
    result = m + n;
    return result;
}

function minus(m, n){
    let result;
    result = m - n;
    return result;
}

function times(m, n){
    let result;
    result = m * n;
    return result;
}

function div(m, n){
    let result;
    result = m / n;
    return result;
}

function mod(m, n){
    let result;
    result = m % n;
    return result;  
}
